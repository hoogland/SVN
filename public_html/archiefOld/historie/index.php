        <h1>Het bestuur door de jaren heen</h1>

     <P>Bij de oprichting van de schaakvereniging op 9 maart
        1973 werd een bestuur gevormd bestaande uit: C. Vis (voorzitter), N. v. d.
        Wetering (secrataris) en A. c.d. Dussen (penningmeester. Een aparte
        wedstijdleider was er nog niet, evenmin als een duidelijke competitie. Er werd
        0m de 14 dagen geschaakt min of meer om "des keizers baard" en men nam zelf
        bord en stukken mee. Waren er bij de oprichting 8 ge&iuml;nteresseerde schakers
        aanwezig, na enkele maanden waren er circa 20 leden.</P> 
     <P>In augustus 1973 werd er besoten deel te nemen aan
        de RSB competitie en kwam P. vd Jeugd als wedstrijdleider in het bestuur. Ook
        werd besloten om 10 spellen met klokken aan te schaffen. De contributie werd
        vastgesteld op 60 gulden. Overigens de kampioenen werden toen nog gehuldigd met
        een bosje bloemen.<BR> <BR> In 1974 verwisselden v.d. Wetering en v.d.van
        functie. Tevens voerde v.d. Wetering het ratingsysteem in. Daarnaast werden
        statuten en reglementen opgesteld.<BR> <BR> In 1975 trad Vis af als voorzitter
        en volgt v.d. Jeugd hem op. Tevens vervult hij dat jaar een dubbelrol door ook
        nog secretaris te bijven.Smorenburg wordt benoemd tot propagandafunctionaris (=
        stukjes schrijven in de krant) en jeugdleider. Samen met C. v.d. Dussen zet
        Smorenburg een clubblad op. Eind 1975 was het aantal leden gegroeid tot 29.<BR>
        <BR> In mei 1976 vertrekt v.d. Wetering an besluit A. v.d. Dussen af te treden.
        Op 1 juni (!) wordt er een algemene ledenvergadering gehouden, waarbij het
        bestuur als volgt wordt samen gesteld: Voorzitter v.d. Jeugd, secretaris
        Beemer, wedstrijdleider Smorenburg en penningmeester N. v.d. Dussen<BR> <BR> Na
        deze turbulente beginjaren, waarin veel opgezet moest worden en met veel
        wisselingen in het bestuur treedt nu een stabiele periode aan. In 12 jaar tijd
        werden slechts 2 bestuursleden opgevolgtt namelijk Beemer en v.d. Dussen door
        Balm en de Koning.<BR> <BR> Na 1988 nemen de wisselingen in het bestuur weer
        toe en neemt de gemiddelde verblijftijd in het bestuur weer af.<BR> <BR> Nog
        enkele opmerkingen<BR> Van der Jeugd heeft het langst in het bestuur gezeten
        namelijk 19 jaar!! Tevens heeft hij daarbinnen 3 functies vervuld, namelijk die
        van wedstrijdleider, secretaris en penning meester.<BR> <BR> Smorenburg heeft
        15 jaar in het bestuur gezeten en ook dezelfde functies als v.d. Jeugd
        gehad.<BR> <BR> Andere bestuursleden met meerdere functies zijn: Balm
        (secrataris &amp; penningmeester) , Koote (wedstrijdleider &amp; voorzitter) en
        Hoogland (jeugdleider &amp; voorzitter)<BR> <BR> Vanaf 1989 wordt jeugdleider
        een offici&euml;le bestuursfunctie. Daarvoor hadden onder andere C. v.d.
        Dussen, Smorenburg, Balm en de Koning zich af en toe over de jeugd ontfermd.
        Ook is het enkele jaren voorgekomen, dat er geen jeugdschaak was. Een lijst van
        de bestuursleden door de jaren heen is hieronder opgenomen. </P> 
